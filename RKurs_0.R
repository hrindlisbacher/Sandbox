# Man kann R als Taschenrechner benutzen und in der
# Konsole diverse Befehle eingeben. Alternativ kann
# man ein Skriptfile "Filename.R" anlegen und darin
# Anweisungen und Kommentare aufschreiben.

# Dies ist ein erstes Skript-File.
# Kommentare beginnen mit dem Zeichen #.
# Wenn man eine oder mehrere Anweisungen markiert
# und im Menu auf "Ausfuehren" klickt oder die
# Tastenkombination
#     "Command + Return" (Mac OS)
# bzw.
#     "Control + R" (Windows)
# drueckt, werden diese Anweisungen in die Konsole
# kopiert und ausgefuehrt.

# Einfache Rechnungen:
3 + 12
23*67
2^12

# Vorsicht: Computer koennen nicht (alles) rechnen!
# Beispiele:
2^12 + 1 - 2^12
2^100 + 1 - 2^100
# !?
1/3 + 1/3 + 1/3 - 1
1/3 + 1/3 - 1 + 1/3
1/3 - 1 + 1/3 + 1/3
# !?

